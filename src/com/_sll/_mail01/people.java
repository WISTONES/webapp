package com._sll._mail01;

public class people {
    private int id;
    private String name;
    private String sex;
    private int pwd;
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getName() {
        return name;
    }

    public String getSex() {
        return sex;
    }

    public void setPwd(int pwd) {
        this.pwd = pwd;
    }

    public int getPwd() {
        return pwd;
    }
}
