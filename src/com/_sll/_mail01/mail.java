package com._sll._mail01;

public class mail {
    private int id;
    private String post;
    private String get;

    public void setId(int id) {
        this.id = id;
    }

    public void setPost(String post) {
        this.post = post;
    }

    public void setGet(String get) {
        this.get = get;
    }

    public int getId() {
        return id;
    }

    public String getPost() {
        return post;
    }

    public String getGet() {
        return get;
    }
}
